function farmerinfo(){
        document.getElementsByClassName("farmerinfo")[0].style.display="block";
        document.getElementById("show").style.display="none";
        document.getElementById("hide").style.display="block";
}

function farmerinfohide(){
        document.getElementsByClassName("farmerinfo")[0].style.display="none";
        document.getElementById("show").style.display="block";
        document.getElementById("hide").style.display="none";
}

function enquiryshow(){
    document.getElementsByClassName("enquiry")[0].style.display="block";
    document.getElementById("showenq").style.display="none";
    document.getElementById("hideenq").style.display="block";
}

function enquiryhide(){
    document.getElementsByClassName("enquiry")[0].style.display="none";
    document.getElementById("showenq").style.display="block";
    document.getElementById("hideenq").style.display="none";
}